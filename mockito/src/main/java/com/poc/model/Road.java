package com.poc.model;

public class Road {

    private boolean hasRoadHole;

    private boolean hasSlopeStreet;

    public boolean isHasRoadHole() {
        return hasRoadHole;
    }

    public void setHasRoadHole(boolean hasRoadHole) {
        this.hasRoadHole = hasRoadHole;
    }

    public boolean isHasSlopeStreet() {
        return hasSlopeStreet;
    }

    public void setHasSlopeStreet(boolean hasSlopeStreet) {
        this.hasSlopeStreet = hasSlopeStreet;
    }
}
